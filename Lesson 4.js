// Operators ka use calculations aur comparisons ke liye hota hai.
// ✅ Arithmetic Operators (Maths ke liye)
let a = 10;
let b = 5;

console.log(a + b);  // Addition (10 + 5 = 15)
console.log(a - b);  // Subtraction (10 - 5 = 5)
console.log(a * b);  // Multiplication (10 * 5 = 50)
console.log(a / b);  // Division (10 / 5 = 2)
console.log(a % b);  // Modulus (Remainder = 0)
// Output:
// 15
// 5
// 50
// 2
// 0


// ✅ Comparison Operators (Values Compare Karne ke liye)
console.log(10 > 5);  // true
console.log(10 < 5);  // false
console.log(10 == 5); // false
console.log(10 != 5); // true
console.log(10 === "10"); // false (Strict Comparison)
// Output:
// true
// false
// false
// true
// false

