// ‚úÖ Basic If-Else Example
let age = 18;

if (age >= 18) {
    console.log("You are eligible to vote!");
} else {
    console.log("You are not eligible to vote.");
}

// Output:
// You are not eligible to vote.

// ‚úÖ Else If (Multiple Conditions)
let marks = 85;

if (marks >= 90) {
    console.log("Grade: A+");
} else if (marks >= 80) {
    console.log("Grade: A");
} else if (marks >= 70) {
    console.log("Grade: B");
} else {
    console.log("Fail");
}

